						<h6><span style="color: #f46842;">Enstrümanın yapısı:</span></h6>
							<p> Trompet, bir ağızlık ve kendi üstüne kıvrılmış silindir bir borudan oluşan nefesli çalgıdır.Bakır ailesi enstrümanlarının 3 temel bölümü vardır: Ağızlık, boru, ve kalak, günümüz pistonlu bakırlarında piston kısmı, boru kısmının üzerindedir. Ayrıca ağızlığı, boruya bağlayan kısma ‘backbore’ ya da ağızlık borusu denir.
                            Trompetin ağızlığı kupa biçimindedir ve kornonun ağızlığından daha ağırdır. Kornonun huni biçimindeki ağızlığına karşın, sığ olan trompet ağızlığı, trompete parlak ses rengini verir. Barok ya da bilinen adıyla Pikolo (piccolo) trompetin ağızlığı modern-kromatik trompetlerin ağızlıklarından daha da sığdır. Ağızlığın küçük ve sığ olması, çalıcıya ayrıca üst bölgelerde, yani tiz sesleri çıkarmada kolaylıklar sağlar.
                            Ağızlık seçimi, çalıcının dudak şekline, çalarkenki rahatlığına ve eserin karakterine göre değişir. Çok kaba bir örnekle senfonik bir eserde daha koyu tonlu, daha derin bir ağızlık kullanılırken, caz trompetçileri sıklıkla daha sığ, parlak ve cılız tonlu bir ağızlık kullanmayı yeğlerler.</p>
						    <p>Trompet, ‘bir ucu açık boru’ sınıfına girer. Çünkü ağızlık kısmında çalıcının dudakları borunun bir ucunu kapamaktadır. Bir ucu açık borularda kuşkusuz ki, bir doğuşkanlar dizisi vardır. Ama bunlar tek sayı doğuşkanlardır. (1,3,5,7... v.b.).Trompetin yapımında silindirik ve konik borular birleştirilmiştir. Bu yüzden rezonans spectrumu da hem silindirik hem konik boruların özelliklerini birlikte taşır. Ayrıca ağızlık ve kalak da rezonans frekanslarını etkiler. (Trompette bazı çift sayılı doğuşkanların çıkması böyle açıklanabilir.) </p>
						    <h6><span style="color: #f46842;">Enstrümanın türleri:</span></h6>
<p>Naturel trompet
İçinde belirli oranlarda bakır ve pirinç olan, belli bir uzunluk ve kalınlıkta işlenmiş bir boruya ağızlık ve kalak kısımlarının eklenmesiyle oluşturulmuş trompetlere, ‘naturel trompetler’ denmektedir. Tarihi filmlerde kralın karşılaması törenlerini gösteren sahnelerde çalınan trompetler naturel trompetlerdir. Gerçekte bu trompetler temel yapısıyla eski Mısır’daki trompetlerinden pek de farklı değildi. Ancak temel bir sesin doğuşkanlarını çıkaran naturel trompetlerle değişik müzikal sesleri elde etmek olanaksızdı. Bu yüzden trompetçiler, yanlarında farklı uzunluklarda trompetler taşırlar ve farklı sesler üflemek gerektiği zaman trompet değiştirirlerdi. Her trompetin çıkardığı temel ses ve doğuşkanları farklı olduğundan, değiştirilen trompetlerin art arda çalınmasıyla müzikal bir zenginliğe ulaşma olanağı sağlanmış oluyordu.

Kromatik (Pistonlu) trompet
Barok çağda Bach ve Haendel, trompetin tiz ses bölgelerini kullandılar. Trompetçiler 21. doğuşkana kadar çıkabildikleri “Clarino Tekniği” denilen bir teknik geliştirdiler. Trompetin günümüzdeki biçimini alabilmesi için 19. yy’a kadar beklemek gerekti. 1826 yılında Bluhmel ve Stölzel adlı iki Alman tarafından basit trompete, 3 adet piston ve bu pistonlara uyumlu boruların eklenmesiyle, “Pistonlu ya da Kromatik Trompet” icat edilmiş oldu. Bu trompetle günümüzdeki trompetlerden farksız olarak bütün kromatik sesleri elde etmek mümkün hale geldi. İlk olarak 1826’da Berlioz, 1827’de Waverley, 1829’da Rossini, trompeti eserlerinde kullanmıştır. Pistonlu trompet için yazılan ilk solo eser Prag Konservatuvarında Trompet ve Trombon Profesörü Kail tarafından piyano/orkestra eşliği için yazılmıştır (1827).</p>
						
						
						
